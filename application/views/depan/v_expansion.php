                  <h3 style="font-size: 25px;">HOW DOES A FABRIC EXPANSION JOINT WORK?</h3>
                  <p>A fabric expansion joint is inserted into a gap in the ductwork where the movement will occur. </p>
                  <p>A fabric expansion joint has two main components:The fabric gas seal and The metal frames. </p>
                  <p>The fabric gas seal is a closed loop, like a belt, with its two edges clamped all around to the metal frames that are in turn connected to the end of the duct. As the ducting moves, the fabric belt deforms. </p>
                  <p>The fabric material must do this without tearing or leaking while sometimes being exposed to high temperatures and/or corrosive media.</p>
                  <p>In some instances, additional components such as insulation pillows, accumulation barriers or ﬂow liners are utilized to help protect the fabric material. </p>

